# --- ШАГ 1: Проверка и коммит локальных изменений ---
Write-Host "Checking local git status..." -ForegroundColor Cyan
$gitStatus = & git status --porcelain
if ($gitStatus) {
    Write-Host "Detected unstaged / uncommitted changes:" -ForegroundColor Yellow
    Write-Host $gitStatus
    Write-Host "Adding and committing..." -ForegroundColor Cyan
    & git add .
    & git commit -m "chore: auto-commit before remote build/deploy"
} else {
    Write-Host "Local working directory is clean." -ForegroundColor Green
}

# --- ШАГ 2: Push локальных изменений на GitHub ---
Write-Host "Pushing changes to GitHub..." -ForegroundColor Cyan
& git push origin main

# Получаем текущий SHA-хеш коммита
$commitSha = (& git rev-parse HEAD).Trim()
Write-Host "Active commit SHA: $commitSha" -ForegroundColor Yellow

# --- ШАГ 3: SSH-подключение и синхронизация Git на удаленном сервере ---
Write-Host "`nConnecting to remote server to sync code..." -ForegroundColor Cyan
$syncCmd = @"
set -e
cd $RemotePath
git fetch origin
git reset --hard $commitSha
echo "HEAD on remote is now at: $commitSha"
"@
$syncCmdClean = $syncCmd -replace "`r", ""
ssh $RemoteHost $syncCmdClean
if ($LASTEXITCODE -ne 0) {
    Write-Host "Git sync on remote server failed!" -ForegroundColor Red
    exit 1
}
Write-Host "Remote Git sync: OK" -ForegroundColor Green

# --- ШАГ 4: Выполнение действия в зависимости от -Action ---
if ($Action -eq "k3s") {
    Write-Host "`n=== MODE: Deploying to Kubernetes (k3s) ===" -ForegroundColor Cyan
    $tmpTar = "/tmp/etl-tool-k3s-deploy.tar"
    $k3sCmd = @"
set -e
cd $RemotePath
echo '[1/3] Building runtime Docker image $ImageName ...'
docker build -f Dockerfile -t $ImageName .

echo '[2/3] Exporting image to $tmpTar ...'
rm -f $tmpTar
docker save --format=docker-archive -o $tmpTar $ImageName

echo '[3/3] Importing image into k3s containerd (namespace k8s.io) ...'
sudo /usr/local/bin/k3s ctr -n k8s.io images import $tmpTar

# Убираем localhost/ префикс если он создался при импорте
if sudo /usr/local/bin/k3s ctr -n k8s.io images list | grep -q "localhost/$ImageName"; then
    echo "Tagging localhost/$ImageName as $ImageName ..."
    sudo /usr/local/bin/k3s ctr -n k8s.io images tag localhost/$ImageName $ImageName
fi

rm -f $tmpTar
echo '=== K3S DEPLOY SUCCESSFUL ==='
"@
    $k3sCmdClean = $k3sCmd -replace "`r", ""
    ssh -o ServerAliveInterval=30 -o ServerAliveCountMax=20 $RemoteHost $k3sCmdClean
}
elseif ($Action -eq "run") {
    Write-Host "`n=== MODE: Running on Remote Server ===" -ForegroundColor Cyan
    
    # Сначала собираем образ для рана (через оригинальный Dockerfile)
    $runBuildCmd = @"
set -e
cd $RemotePath
echo '[1/2] Building run Docker image $ImageName ...'
docker build -f Dockerfile -t $ImageName .
"@
    $runBuildCmdClean = $runBuildCmd -replace "`r", ""
    ssh -o ServerAliveInterval=30 -o ServerAliveCountMax=20 $RemoteHost $runBuildCmdClean
    if ($LASTEXITCODE -ne 0) {
        Write-Host "Docker image build failed!" -ForegroundColor Red
        exit 1
    }

    # Поиск нужного пайплайна
    $selectedPipelines = $partitions.Keys | Sort-Object
    if ($Table -ne "") {
        $selectedPipelines = $selectedPipelines | Where-Object { $_ -like "*$Table*" }
        if (-not $selectedPipelines) {
            Write-Host "ERROR: No pipeline matching '$Table'" -ForegroundColor Red
            exit 1
        }
    }

    Write-Host "Launching pipelines: $($selectedPipelines.Count)" -ForegroundColor Yellow
    foreach ($cfg in $selectedPipelines) {
        $parts          = $partitions[$cfg] -join " "
        $containerName  = "etl-" + ($cfg -replace "stg_to_ods_|\.yaml","")
        Write-Host "  Starting container: $containerName for config: $cfg" -ForegroundColor Cyan

        $runContainerCmd = @"
docker rm -f $containerName 2>/dev/null || true
docker run -d --name $containerName --restart=no --net=host \
  --entrypoint bash \
  -e SPARK_LOCAL_HOSTNAME=localhost \
  -e SPARK_LOCAL_IP=127.0.0.1 \
  -e DISABLE_CERT_CHECKING=true \
  -e 'JAVA_TOOL_OPTIONS=-Xmx4g -Xms1g -XX:MaxMetaspaceSize=512m -XX:-ShowCodeDetailsInExceptionMessages -XX:TieredStopAtLevel=1 -Dfile.encoding=UTF-8 -Dcom.amazonaws.sdk.disableCertChecking=true' \
  $ImageName \
  /app/run_all_partitions.sh $cfg $parts
"@
        $runContainerCmdClean = $runContainerCmd -replace "`r", ""
        ssh $RemoteHost $runContainerCmdClean | Out-Null
        Write-Host "  Container $containerName successfully started." -ForegroundColor Green
    }
    Write-Host "`n=== All pipelines launched on remote server ===" -ForegroundColor Green
}
